    private void btnPromjenaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPromjenaActionPerformed
        greskaIme.setText("");
        greskaOIB.setText("");
        greskaPrezime.setText("");
        greskaRacun.setText("");
        
        korisnik = new Korisnik();
        
        boolean provjeraIme = false;
        boolean provjeraPrezime = false;
        boolean provjeraRacun=true;
        int provjeraOIB = 0;
        
        int red = tabelica.getSelectedRow();
        
         provjeraIme = Provjera.provjeriJelText(txtIme.getText());
        provjeraPrezime = Provjera.provjeriJelText(txtPrezime.getText());
        provjeraRacun = Provjera.provjeriJelText(txtRacun.getText());
        provjeraOIB = txtOIB.getText().trim().length();
        
        if(provjeraOIB != 11)
        {
            greskaOIB.setText("ERROR");
        }
        if(provjeraIme==false)
        {
            greskaIme.setText("ERROR");
        }
        if(provjeraRacun==true)
        {
            greskaRacun.setText("ERROR");
        }
       
        
        if(provjeraPrezime==false)
        {
            greskaPrezime.setText("ERROR");
        }
        if(provjeraIme==true && provjeraPrezime==true && provjeraRacun==false && provjeraOIB==11) {
        korisnik.setSifra(red + 1);
        korisnik.setIme(txtIme.getText().substring(0,1).toUpperCase() +txtIme.getText().substring(1).toLowerCase());
        korisnik.setPrezime(txtPrezime.getText().substring(0,1).toUpperCase() + txtPrezime.getText().substring(1).toLowerCase());
        korisnik.setOib(txtOIB.getText());
        korisnik.setRacun(txtRacun.getText());
        obrada.promjenaKorisnika(korisnik);
        ispis();
        }

    }//GEN-LAST:event_btnPromjenaActionPerformed

