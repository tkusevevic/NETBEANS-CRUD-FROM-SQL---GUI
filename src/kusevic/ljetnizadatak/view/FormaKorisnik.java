    private void ispis() {
        lista = new ArrayList<>(obrada.getKorisnici());
        DefaultTableModel model = (DefaultTableModel) tabelica.getModel();
        model.setColumnIdentifiers(new Object[]{"Ime", "Prezime", "OIB", "Racun"});
        model.setRowCount(0);
        Object[] red = new Object[4];
        for (int i = 0; i < lista.size(); i++) {
            red[0] = lista.get(i).getIme();
            red[1] = lista.get(i).getPrezime();
            red[2] = lista.get(i).getOib();
            red[3] = lista.get(i).getRacun();
            model.addRow(red);

        }

    }

